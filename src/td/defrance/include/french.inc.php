    <h2 id="histoire"> Histoire de PHP</h2>
    <p>PHP est un langage de script utilisé le plus souvent côté serveur : dans cette architecture,
        le serveur interprète le code PHP des pages web demandées et génère du code (HTML, XHTML, CSS par exemple)
        et des données (JPEG, GIF, PNG par exemple) pouvant être interprétés et rendus par un navigateur web.
        PHP peut également générer d'autres formats comme le WML, le SVG et le PDF.</p>

    <p>Il a été conçu pour permettre la création d'applications dynamiques, le plus souvent développées pour le Web.
        PHP est le plus souvent couplé à un serveur Apache bien qu'il puisse être installé sur la plupart des serveurs
        HTTP tels que IIS ou nginx. Ce couplage permet de récupérer des informations issues d'une base de données,
        d'un système de fichiers (contenu de fichiers et de l'arborescence) ou plus simplement des données envoyées par
        le navigateur afin d'être interprétées ou stockées pour une utilisation ultérieure.</p>

    <p>C'est un langage peu typé et souple et donc facile à apprendre par un débutant mais, de ce fait, des bugs et des
        failles de sécurité peuvent rapidement apparaître dans les applications. Pragmatique, PHP ne s'encombre pas de
        théorie et a tendance à choisir le chemin le plus direct. Néanmoins, le nom des fonctions (ainsi que le passage
        des arguments) ne respecte pas une logique uniforme, ce qui peut être préjudiciable à l'apprentissage. </p>

    <p class="small">Selon <a href="https://fr.wikipedia.org/wiki/PHP">Wikipédia</a>.</p>


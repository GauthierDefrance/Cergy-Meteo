    <h2 id="histoire"> History of PHP</h2>
    <p>PHP is a scripting language most often used on the server side: in this architecture,
        the server interprets the PHP code of the requested web pages and generates code (such
        as HTML, XHTML, CSS) and data (such as JPEG, GIF, PNG) that can be interpreted and
        rendered by a web browser. PHP can also generate other formats like WML, SVG, and PDF.</p>

    <p>It was designed to enable the creation of dynamic applications, most often developed for
        the web. PHP is most commonly paired with an Apache server, although it can be installed
        on most HTTP servers such as IIS or nginx. This pairing allows the retrieval of information
        from a database, a file system (content of files and the directory structure), or simply
        data sent by the browser to be interpreted or stored for later use.</p>

    <p>It is a loosely typed and flexible language, making it easy for beginners to learn. However,
        because of this, bugs and security vulnerabilities can quickly appear in applications.
        Pragmatic, PHP does not burden itself with theory and tends to take the most direct path.
        Nevertheless, the names of functions (as well as argument passing) do not follow a consistent
        logic, which can be detrimental to learning.</p>

